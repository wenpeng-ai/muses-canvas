export function getCanvasPageCopy(messages: AppMessages) {
  return {
    ...enMessages.copy.canvasPage,
    ...(messages.copy.canvasPage ?? {}),
  } as CanvasPageCopy;
}

export function formatCopy(
  template: string,
  values: Record<string, string | number>,
) {
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template,
  );
}
